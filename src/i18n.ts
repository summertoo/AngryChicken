const all: Record<string, Record<string, string>> = { zh, en };

class I18n {
  private lang: Lang = 'zh';
  private listeners: Set<() => void> = new Set();

  constructor() {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved === 'en' || saved === 'zh') this.lang = saved;
  }

  get(): Lang {
    return this.lang;
  }

  t(key: string, params?: Record<string, string | number>): string {
    const map = all[this.lang];
    let text = map[key] ?? key;
    if (params) {
      for (const [k, v] of Object.entries(params)) {
        text = text.replace(`{${k}}`, String(v));
      }
    }
    return text;
  }

  levelName(level: { id: number }): string {
    return this.t(`level.name.${level.id}`);
  }

  set(lang: Lang): void {
    if (lang === this.lang) return;
    this.lang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    this.listeners.forEach((fn) => fn());
  }

  toggle(): Lang {
    const next: Lang = this.lang === 'zh' ? 'en' : 'zh';
    this.set(next);
    return next;
  }

  onChange(fn: () => void): void {
    this.listeners.add(fn);
  }
}

export const i18n = new I18n();
